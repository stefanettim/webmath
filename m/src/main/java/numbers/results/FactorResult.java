package numbers.results;

public class FactorResult {

	public int factor = 0;
	public int power = 0;
	
	
	
	public FactorResult(int factor, int power) {
		super();
		this.factor = factor;
		this.power = power;
	}
	public int getFactor() {
		return factor;
	}
	public void setFactor(int factor) {
		this.factor = factor;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	
	
	
}
